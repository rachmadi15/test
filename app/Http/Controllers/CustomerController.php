    public function getDetailCustomer($cutomerId=0){

        $data['data'] = Sale::orderby("total_sales","asc")
        			->select('id','total_sales')
        			->where('customer_id',$cutomerId)
        			->get();
        $data['alamat'] = Alamat::orderby("alamat","asc")
                    ->select('id','alamat')
                    ->where('customer_id',$cutomerId)
                    ->get();
        return response()->json($data);
    }

